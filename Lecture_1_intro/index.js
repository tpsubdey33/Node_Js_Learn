// -----------------------------------------------
// 📌 Initializing a Project with NPM
// -----------------------------------------------
// Every Node.js project should be initialized with a package.json file.
// This file acts as a manifest (metadata + dependencies).
//
// ▶️ Step 1: Run inside project root
//    npm init
//
// ▶️ Step 2: Answer these questions:
// 1. package name → (default = folder name)
// 2. version → (default: 1.0.0)
// 3. description → Small intro
// 4. entry point → (default: index.js)
// 5. test command → leave empty
// 6. git repository → optional
// 7. keywords → optional
// 8. author → your name
// 9. license → (default: ISC)
//
// ▶️ Shortcut:
//    npm init -y   (accepts all defaults)
//
// Example package.json:
//
// {
//   "name": "my-app",
//   "version": "1.0.0",
//   "description": "This is my first Node.js project",
//   "main": "index.js",
//   "scripts": {
//     "test": "echo \"Error: no test specified\" && exit 1"
//   },
//   "author": "Your Name",
//   "license": "ISC"
// }
